public class DataType {
    public static void main(String[] args) {
        int int_type = 10;
        double double_type = 10.584839382939;
        float float_type = 10.5f;
        char char_type = 'a';
        short short_type = 8;
        boolean boolean_type = true;
        long long_type = 1055555555555444L;
        byte byte_type = 7;
        System.out.println("Integer data type: " + int_type); // Integer data type: 10
        System.out.println("Double data type: " + double_type); // Double data type: 10.584839382939
        System.out.println("Float data type: " + float_type); // Float data type: 10.5
        System.out.println("Char data type: " + char_type); // Char data type: a
        System.out.println("Long data type: " + long_type); // Long data type: 1055555555555444
        System.out.println("Byte data type: " + byte_type); // Byte data type: 7
        System.out.println("Boolean data type: " + boolean_type); //Boolean data type: true
        System.out.println("Short data type: " + short_type); // Short data type: 8
    }
}